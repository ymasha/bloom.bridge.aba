const languageSelect = document.querySelector("#language-select");
const descriptionMeta = document.querySelector('meta[name="description"]');

function getValueByPath(source, path) {
  return path.split(".").reduce((value, key) => (value ? value[key] : undefined), source);
}

function applyLanguage(language) {
  const dictionary = translations[language] || translations.en;

  document.documentElement.lang = language;
  document.title = dictionary.meta.title;
  descriptionMeta.setAttribute("content", dictionary.meta.description);

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    const value = getValueByPath(dictionary, key);

    if (typeof value === "string") {
      if (value.includes("<br")) {
        element.innerHTML = value;
      } else {
        element.textContent = value;
      }
    }
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
    const key = element.dataset.i18nPlaceholder;
    const value = getValueByPath(dictionary, key);

    if (typeof value === "string") {
      element.setAttribute("placeholder", value);
    }
  });

  languageSelect.value = language;
  localStorage.setItem("bloomBridgeLanguage", language);
}

const savedLanguage = localStorage.getItem("bloomBridgeLanguage");
applyLanguage(savedLanguage && translations[savedLanguage] ? savedLanguage : "en");

languageSelect.addEventListener("change", (event) => {
  applyLanguage(event.target.value);
});

const contactForm = document.querySelector("#contact-form");

if (contactForm) {
  contactForm.addEventListener("submit", (event) => {
    event.preventDefault();

    const formData = new FormData(contactForm);
    const name = formData.get("name") || "";
    const email = formData.get("email") || "";
    const phone = formData.get("phone") || "";
    const subject = formData.get("subject") || "Website inquiry";
    const availability = formData.get("availability") || "";
    const message = formData.get("message") || "";

    const body = [
      `Name: ${name}`,
      `Email: ${email}`,
      `Phone: ${phone}`,
      `Availability: ${availability}`,
      "",
      "Message:",
      `${message}`,
    ].join("\n");

    const mailtoUrl = `mailto:info@bloombridgeaba.com?subject=${encodeURIComponent(subject)}&body=${encodeURIComponent(body)}`;
    window.location.href = mailtoUrl;
  });
}
